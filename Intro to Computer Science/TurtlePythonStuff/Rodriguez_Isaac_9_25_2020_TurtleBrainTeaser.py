import turtle as t
t.penup()
t.setpos(-200, 200)
t.dot(50, "black")

t.penup()
t.setpos(0, 200)
t.dot(50, "black")

t.penup()
t.setpos(200, 200)
t.dot(50, "black")

t.penup()
t.setpos(0, 0)
t.dot(50, "black")

t.penup()
t.setpos(-200, 0)
t.dot(50, "black")

t.penup()
t.setpos(200, 0)
t.dot(50, "black")

t.penup()
t.setpos(0, -200)
t.dot(50, "black")

t.penup()
t.setpos(-200, -200)
t.dot(50, "black")

t.penup()
t.setpos(200, -200)
t.dot(50, "black")

t.penup()
t.goto(200,-200)

t.pendown()
t.goto(-200,200)
t.goto(-200,-400)
t.goto(430,200)
t.goto(-200,200)

t.done()